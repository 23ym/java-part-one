public class IntegerManipulations {
    public static void main(String[] args) {
        int a = 5;
        int b = 9;
        int c = a + b;
        c = c + 5;
        c = c * 10;
        c *= 10;
        a = 10;
        b = 3;
        c = a;
        c = a % b;
        c += 5;
        System.out.println("a + b = " + c);
    }
}
